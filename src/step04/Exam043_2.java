package step04;

public class Exam043_2 {
  public static void main(String[] args) {
    int a = 0b1100;

    System.out.println(a >>> 1);
    System.out.println(a >>> 2);
    System.out.println(a >>> 3);

    int b = -12;
    System.out.println(b >>> 1);
    System.out.println(b >>> 2);
    System.out.println(b >>> 3);
  }
}


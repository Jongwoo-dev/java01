//주제 : Literal - 문자 코드표의 진화.

package step02;

public class Exam031 {
  public static void main(String[] args) {
    System.out.println(true);
    System.out.println(false);

    if (true)
      System.out.println("True이다.");

    if (10 > 20)    // false
      System.out.println("10 > 20");
    else
      System.out.println("10 <= 20");
  }
}
